import { model, Schema } from 'mongoose';
import { User } from '@interfaces/users.interface';

const userSchema : Schema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export default userSchema;
